// รักหนูให้เหมือนสูตรคูณต้อง มีแต่เพิ่มพูนไม่มีลดน้อยลงนะ ... เข้าเรื่องดีกว่า สูตรคูณเป็นตารางกริดที่หัวแถวและคอลัมน์เป็นเลขที่จะนำมาคูณ และตัวเลขในแต่ละช่องคือผลคูณดังกล่าว โดยพี่จะให้น้องๆเขียน function ในการแสดงแม่สูตรโดยห้ามใช้แค่ function main เพียง function เดียว

#include <stdio.h>

int main () {
    int mae;
    scanf("%d", &mae);

    for(int i = 1; i <= 12; i++) {
        printf("%d * %d = %d\n", mae, i, mae * i);
    }
}