int main () {
    float height, weight, bmi;

    printf("Weight (kg) : ");
    scanf("%f", &weight);

    printf("Height (m) : ");
    scanf("%f", &height);

    bmi = weight / pow(height, 2);
    printf("Your BMI : %f\n", bmi);

    if(bmi >= 30){
        printf("fatty üíÄ");
    }
    else if (bmi >= 25){
        printf("more than average");
    }
    else if (bmi >= 18.5){
        printf("normal");
    }
    else if (bmi < 18.5){
        printf("too thin üíÄ");
    }
}